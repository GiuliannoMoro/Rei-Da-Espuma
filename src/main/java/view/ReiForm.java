    //ComboBox - Dono do Veículo
    public void carregarClientes(){
        
        ClienteDAO cl = new ClienteDAO();
        ArrayList<Cliente> lista = cl.listar();
        
        cbCliente.removeAllItems();
        
        for(Cliente c : lista){
            cbCliente.addItem(c);   
        }
    }
    //ComboBox - Serviço pegando a Placa
    public void carregarVeiculo(){
        
        VeiculoDAO vw = new VeiculoDAO();
        ArrayList<Veiculo> lista = vw.listar();
        
        cbVeiculo.removeAllItems();
        
        for(Veiculo v : lista){
            cbVeiculo.addItem(v);
        }
 }     
   //ComboBox - Serviços
    public void carregarTipoServico(){
        
        cbTipoServico.removeAllItems();
        cbTipoServico.addItem("Lavagem Simples - R$ 100,00");
        cbTipoServico.addItem("Lavagem Completa - R$ 150,00");
        cbTipoServico.addItem("Lavagem de Luxo - R$ 200,00");
    }
    // TABELA
    public void carregarTabela() {

    ServicoDAO sd = new ServicoDAO();

    EspumaTableModel modelo =
            new EspumaTableModel(sd.listarTabela());

    tbDados.setModel(modelo);
}
    // Pesquisa
    public void carregarPesquisa(String texto) {

    ServicoDAO sd = new ServicoDAO();

    EspumaTableModel modelo =
            new EspumaTableModel(sd.pesquisar(texto));

    tbDados.setModel(modelo);
}
