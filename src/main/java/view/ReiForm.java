    private void btnAtualizarVeiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarVeiculoActionPerformed
        
        Veiculo veiculoSelecionado =
            (Veiculo) cbVeiculo.getSelectedItem();

    Cliente clienteSelecionado =
            (Cliente) cbCliente.getSelectedItem();

    if (veiculoSelecionado == null) {

        JOptionPane.showMessageDialog(
                null,
                "Selecione um veículo!"
        );

        return;
    }

    Veiculo v = new Veiculo();

    // MUITO IMPORTANTE
    v.setId(veiculoSelecionado.getId());

    v.setPlaca(txtPlaca.getText());
    v.setMarca(txtMarca.getText());
    v.setModelo(txtModelo.getText());
    v.setCor(txtCor.getText());
    v.setObservacao(txtObsVeiculo.getText());

    v.setIdCliente(clienteSelecionado.getId());

    VeiculoDAO vd = new VeiculoDAO();

    vd.atualizar(v);

    JOptionPane.showMessageDialog(
            null,
            "Veículo atualizado!"
    );
    }//GEN-LAST:event_btnAtualizarVeiculoActionPerformed

